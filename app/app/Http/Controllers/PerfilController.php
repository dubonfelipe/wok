     */
    public function update(Request $request, $id)
    {
        if ($request['password']===$request['password2']) {
            $user = User::find($id);
            $user->password=bcrypt($request['password']);
            $user->save();
            
            $log = [
                'desc'=>'Edito Contraseña de usuario',
                'email'=>\Auth::user()->email,
            ];
            Log::create($log);
            Session::flash('message','La contraseña ha sido actualizada');
            return Redirect::to('/');
        } else {
            Session::flash('message','Las contraseñas ingresadas no coinciden');
            return Redirect::to('/perfil');
        }
    }

